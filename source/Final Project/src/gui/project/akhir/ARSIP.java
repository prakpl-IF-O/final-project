    /**
     * memperbarui tabel jika ada inputan dengan data transaksi
     */
    public void updateTable() {
        try {
            String sql = "select * from arsip_pelanggan left join data_transaksi on arsip_pelanggan.ID=data_transaksi.ID";
            ps = conn.prepareStatement(sql);
            rs = ps.executeQuery();
            FTable.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

