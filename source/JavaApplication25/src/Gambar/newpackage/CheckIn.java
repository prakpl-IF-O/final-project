    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        table1.getDataVector().removeAllElements();
        table1.fireTableDataChanged();
        String a = null;
        Koneksi lokal=new Koneksi();
        try{
            if(TypeBox.getSelectedItem().equals("Regular"))a="Regular";
            else if(TypeBox.getSelectedItem().equals("Premium"))a="Premium";
            else if(TypeBox.getSelectedItem().equals("Suite"))a="Suite";
            else if(TypeBox.getSelectedItem().equals("VIP"))a="VIP";
            Connection c = lokal.getKoneksi();
            Statement s=c.createStatement();
            String sql="select Number,type from room where type= '"+a+"' and customer is null;";
            ResultSet r=s.executeQuery(sql);
            while(r.next()){
                Object[]o=new Object[2];
                o[0]=r.getInt("Number");
                o[1]=r.getString("type");
                table1.addRow(o);
            }
            r.close();
            s.close();
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null,"Connection Failed"+e);
        
        }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

